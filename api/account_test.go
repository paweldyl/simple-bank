func randomAccount(owner string) db.Account {
	return db.Account{
		ID:       util.RandomInt(1, 1000),
		Owner:    owner,
		Balance:  util.RandomMoney(),
		Currency: util.RandomCurrency(),
	}
}

func requireBodyMatchAccount(t *testing.T, body *bytes.Buffer, account db.Account) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var gotAccount db.Account
	err = json.Unmarshal(data, &gotAccount)

	require.NoError(t, err)
	require.Equal(t, account, gotAccount)
}

func requireBodyMatchAccountsList(t *testing.T, body *bytes.Buffer, accountsList []db.Account) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)
	var gotAccountsList []db.Account
	err = json.Unmarshal(data, &gotAccountsList)

	require.NoError(t, err)
	require.Len(t, gotAccountsList, len(accountsList))

	for i := 0; i < len(accountsList); i++ {
		require.Equal(t, accountsList[i], gotAccountsList[i])
	}
}
